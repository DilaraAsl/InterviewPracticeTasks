package com.cydeo.hRank;

public class multiplicationLoop {
    public static void main(String[] args) {
        int n=2;
        for (int i = 1; i <=10; i++) {
            System.out.println(n+"x"+i+""+n*i); // concatenates last two i and n*i
            System.out.println( n+"x"+i+"="+n*i);

        }
    }
}
